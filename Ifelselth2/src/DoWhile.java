public class DoWhile {
    public static void main(String[] args) {

        //angka mulai
        int bensin = 1;

        do {
            //yang mau di looping
            System.out.println(bensin + "Liter");

            //operator unary penambahan
            bensin++;

            //batasan looping
        } while (bensin <= 10) ;


    }
}
