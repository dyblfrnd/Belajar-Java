public class WhileLoop {
    public static void main(String[] args) {
        //angka mulai
        int huruf = 1;

        //batasan looping
        while (huruf <= 10){
            //yang akan di looping
            System.out.println("Assalamulaikum: " + huruf);

            //operator penambahan
            huruf++;
        }
    }
}
